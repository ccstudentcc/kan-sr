import argparse
import csv
import hashlib
import json
from pathlib import Path
from typing import Any, Dict, List, Tuple

REQUIRED_OUTPUT_FIELDS = [
    "task_name",
    "method",
    "seed",
    "mse",
    "r2",
    "time_seconds",
    "expression",
    "complexity",
    "status",
    "error_message",
]


def parse_args() -> argparse.Namespace:
    """Parse CLI arguments for the placeholder run executor."""
    parser = argparse.ArgumentParser(
        description="Execute a placeholder run stage from a plan file and emit raw outputs."
    )
    parser.add_argument(
        "--plan",
        type=Path,
        required=True,
        help="Path to a plan file generated by run_experiment_plan.py (.json or .csv).",
    )
    parser.add_argument(
        "--out-dir",
        type=Path,
        default=Path("output"),
        help="Root output directory where raw results are written.",
    )
    return parser.parse_args()


def load_plan(plan_path: Path) -> List[Dict[str, Any]]:
    """Load plan records from JSON or CSV.

    Args:
        plan_path: Plan file path.

    Returns:
        A list of plan rows as dictionaries.

    Raises:
        ValueError: If the file extension is unsupported.
    """
    suffix = plan_path.suffix.lower()
    if suffix == ".json":
        with plan_path.open("r", encoding="utf-8") as f:
            data = json.load(f)
        if not isinstance(data, list):
            raise ValueError(f"JSON plan must be a list of rows: {plan_path}")
        return data

    if suffix == ".csv":
        with plan_path.open("r", encoding="utf-8", newline="") as f:
            reader = csv.DictReader(f)
            return list(reader)

    raise ValueError(f"Unsupported plan file type: {plan_path.suffix}")


def stable_random_01(*parts: Any) -> float:
    """Create a deterministic pseudo-random value in [0, 1)."""
    key = "|".join(str(p) for p in parts)
    digest = hashlib.sha256(key.encode("utf-8")).hexdigest()
    value = int(digest[:12], 16)
    return value / float(16**12)


def simulate_row(plan_row: Dict[str, Any]) -> Dict[str, Any]:
    """Simulate one execution row from one plan row.

    Args:
        plan_row: One row from the plan file.

    Returns:
        A normalized output row matching REQUIRED_OUTPUT_FIELDS.
    """
    task_name = str(plan_row.get("task_name", "unknown_task"))
    method = str(plan_row.get("method", "unknown_method"))
    seed_raw = plan_row.get("seed", "")

    try:
        seed = int(seed_raw)
    except (TypeError, ValueError):
        return {
            "task_name": task_name,
            "method": method,
            "seed": seed_raw,
            "mse": "",
            "r2": "",
            "time_seconds": "",
            "expression": "",
            "complexity": "",
            "status": "fail",
            "error_message": f"Invalid seed value: {seed_raw}",
        }

    rand_a = stable_random_01(task_name, method, seed, "mse")
    rand_b = stable_random_01(task_name, method, seed, "r2")
    rand_c = stable_random_01(task_name, method, seed, "time")
    rand_d = stable_random_01(task_name, method, seed, "complexity")

    mse = 0.001 + rand_a * 0.099
    r2 = 0.80 + rand_b * 0.199
    time_seconds = 0.05 + rand_c * 0.45
    complexity = 5 + int(rand_d * 46)
    expression = f"{method}_placeholder_expr_seed_{seed}"

    return {
        "task_name": task_name,
        "method": method,
        "seed": seed,
        "mse": f"{mse:.6f}",
        "r2": f"{r2:.6f}",
        "time_seconds": f"{time_seconds:.6f}",
        "expression": expression,
        "complexity": complexity,
        "status": "success",
        "error_message": "",
    }


def write_grouped_raw(rows: List[Dict[str, Any]], out_dir: Path) -> List[Path]:
    """Write raw result rows grouped by task and method.

    Args:
        rows: Simulated output rows.
        out_dir: Root output directory.

    Returns:
        List of written CSV paths.
    """
    grouped: Dict[Tuple[str, str], List[Dict[str, Any]]] = {}
    for row in rows:
        key = (str(row["task_name"]), str(row["method"]))
        grouped.setdefault(key, []).append(row)

    written_paths: List[Path] = []
    for (task_name, method), records in grouped.items():
        target_dir = out_dir / "raw" / task_name
        target_dir.mkdir(parents=True, exist_ok=True)
        target_csv = target_dir / f"{method}.csv"

        with target_csv.open("w", encoding="utf-8", newline="") as f:
            writer = csv.DictWriter(f, fieldnames=REQUIRED_OUTPUT_FIELDS)
            writer.writeheader()
            writer.writerows(records)

        written_paths.append(target_csv)

    return written_paths


def main() -> None:
    """Entry point for placeholder run execution."""
    args = parse_args()
    plan_rows = load_plan(args.plan)
    output_rows = [simulate_row(row) for row in plan_rows]
    paths = write_grouped_raw(output_rows, args.out_dir)

    print(f"[OK] Processed plan rows: {len(plan_rows)}")
    print(f"[OK] Written raw files: {len(paths)}")
    for path in paths:
        print(f"[OK] RAW: {path}")


if __name__ == "__main__":
    main()
